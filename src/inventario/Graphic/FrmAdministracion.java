    private boolean displayInformacionProveedores(MyTableModel dbMtm)
    {
        if(dbMtm == null)
        {
            return false;
        }
        
        MyTableModel mtm = new MyTableModel();
        mtm.addColumn("RUT");
        mtm.addColumn("Proveedor");
        mtm.addColumn("Precio");
        mtm.addColumn("Cantidad");
        mtm.addColumn("Factura");
        mtm.addColumn("Fecha");
        
        Object[] data = null;

        for(int i=0; i<dbMtm.getRowCount(); i++)
        {
            data = new Object[6];
            data[0] = dbMtm.getValueAt(i, 0).toString();
            data[1] = dbMtm.getValueAt(i, 1).toString();
            data[2] = dbMtm.getValueAt(i, 2).toString();
            data[3] = dbMtm.getValueAt(i, 3).toString();
            data[4] = dbMtm.getValueAt(i, 4).toString();
            data[5] = dbMtm.getValueAt(i, 5).toString();

            mtm.addRow(data);
        }
        
        tbInformacionProveedores_Proveedores.setModel(mtm);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
        tbInformacionProveedores_Proveedores.getColumnModel().getColumn(5).setCellRenderer(centerRenderer);
        
        return true;
    }
    
