    private void btInformacionProveedores_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInformacionProveedores_ConsultarActionPerformed
        // TODO add your handling code here:
        
        if(txtInformacionProveedores_Codigo.getText().length() > 1)
        {
            Producto producto = LogicController.getProductoByCodigo(txtInformacionProveedores_Codigo.getText());
            if(producto != null)
            {
                MyTableModel dbMtm = LogicController.getProveedoresByProducto(producto.getCodigo());

                if(displayInformacionProveedores(dbMtm) == true)
                {
                    TitledBorder titledBorder = BorderFactory.createTitledBorder("Lista de proveedores para \"" + producto.getNombre() + "\" (código: " + producto.getCodigo() + ")");
                    pnInformacionProveedores_Container.setBorder(titledBorder);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Ha ocurrido un error al mostrar el reporte de proveedores", "Generar reporte", JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No se ha encontrado el producto con código \"" + txtInformacionProveedores_Codigo.getText() + "\"", "Generar reporte", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debes ingresar el código del producto a consultar", "Generar reporte", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btInformacionProveedores_ConsultarActionPerformed

