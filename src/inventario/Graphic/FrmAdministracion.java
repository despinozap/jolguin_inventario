    private void clearForm_InformeCostos()
    {
        int year = Calendar.getInstance().get(Calendar.YEAR);
        
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("Seleccione..");
        for(int i=year; i>year-5; i--)
        {
            dcbm.addElement(i);
        }
        
        cbInformeCostos_Anio.setModel(dcbm);
        
        lbInformeCostos_Chart.setIcon(null);
    }
    
    private void clearForm_Top10Productos()
    {
        Calendar cal = Calendar.getInstance(); 
        cal.setTime(new Date());
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM");
        
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("Seleccione..");
        for(int i=0; i<12; i++)
        {
            dcbm.addElement(dateFormat.format(cal.getTime()));
            
            cal.add(Calendar.MONTH, -1);
        }
        
        cbTop10Productos_Mes.setModel(dcbm);
        
        lbTop10Producto_Chart.setIcon(null);
    }
    
    private boolean clearForm_InformacionInventario()
    {
        MyTableModel dbMtm = LogicController.getProductos();
        if(dbMtm == null)
        {
            return false;
        }
        
        boolean result = false;
        
        MyTableModel mtm = new MyTableModel();
        mtm.addColumn("Código");
        mtm.addColumn("Producto");
        mtm.addColumn("Clasificación");
        mtm.addColumn("Fecha caducidad");
        mtm.addColumn("Cantidad");
        
        Object[] data = null;

        for(int i=0; i<dbMtm.getRowCount(); i++)
        {
            data = new Object[5];
            data[0] = dbMtm.getValueAt(i, 0).toString();
            data[1] = dbMtm.getValueAt(i, 1).toString();
            data[2] = dbMtm.getValueAt(i, 2).toString();
            data[3] = dbMtm.getValueAt(i, 3).toString();
            data[4] = dbMtm.getValueAt(i, 4).toString();

            mtm.addRow(data);
        }
        
        tbInformacionInventario_Productos.setModel(mtm);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        
        tbInformacionInventario_Productos.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tbInformacionInventario_Productos.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        tbInformacionInventario_Productos.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        tbInformacionInventario_Productos.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
        tbInformacionInventario_Productos.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
        
        return result;
    }
    
