    public void loadProductos()
    {
        displayProductos(LogicController.getProductos());
    }
    
    private boolean displayProductos(MyTableModel dbMtm)
    {
        if(dbMtm == null)
        {
            return false;
        }
        
        boolean result = false;
        
        MyTableModel mtm = new MyTableModel();
        mtm.addColumn("Código");
        mtm.addColumn("Producto");
        mtm.addColumn("Clasificación");
        mtm.addColumn("Fecha caducidad");
        mtm.addColumn("Cantidad");
        
        if(dbMtm != null)
        {
            Object[] data = null;
            
            for(int i=0; i<dbMtm.getRowCount(); i++)
            {
                data = new Object[5];
                data[0] = dbMtm.getValueAt(i, 0).toString();
                data[1] = dbMtm.getValueAt(i, 1).toString();
                data[2] = dbMtm.getValueAt(i, 2).toString();
                data[3] = dbMtm.getValueAt(i, 3).toString();
                data[4] = dbMtm.getValueAt(i, 4).toString();
                
                mtm.addRow(data);
            }
        }
        else
        {
            
        }
        
        tbProductos.setModel(mtm);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        
        tbProductos.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tbProductos.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        tbProductos.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        tbProductos.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
        tbProductos.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
        
        return result;
    }
    
    private boolean validateFormProducto()
    {
        boolean response = true;
        
        if(txtNombre.getText().length() < 1)
        {
            JOptionPane.showMessageDialog(this, "Debes ingresar el nombre del producto", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        else if(cbClasificacion.getSelectedIndex() < 1)
        {
            JOptionPane.showMessageDialog(this, "Debes seleccionar la clasificación del producto", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        else if(txtCodigo.getText().length() < 1)
        {
            JOptionPane.showMessageDialog(this, "Debes ingresar el código del producto", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        else if(txtFechaCaducidad.getText().length() < 1)
        {
            JOptionPane.showMessageDialog(this, "Debes ingresar la fecha de caducidad del producto", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        else if(LogicController.getProductoByNombre(txtNombre.getText()) != null)
        {
            JOptionPane.showMessageDialog(this, "Ya existe un producto con el nombre ingresado (" + txtNombre.getText() + ")", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        else if(LogicController.getProductoByCodigo(txtCodigo.getText()) != null)
        {
            JOptionPane.showMessageDialog(this, "Ya existe un producto con el código ingresado (" + txtCodigo.getText() + ")", "Ingresar nuevo producto", JOptionPane.WARNING_MESSAGE);
            
            response = false;
        }
        
        return response;
    }
    
