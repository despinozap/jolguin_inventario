    private void btInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsertActionPerformed
        // TODO add your handling code here:
        if(validateFormProducto())
        {
            boolean executed = LogicController.insertProducto(
                                                                txtCodigo.getText(),
                                                                txtNombre.getText(), 
                                                                cbClasificacion.getSelectedItem().toString(), 
                                                                txtFechaCaducidad.getText()
            );
            
            if(executed == true)
            {
                JOptionPane.showMessageDialog(this, "Se ha ingresado exitosamente el nuevo producto", "Ingresar nuevo producto", JOptionPane.INFORMATION_MESSAGE);

                loadProductos();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error al ingresar el nuevo producto", "Ingresar nuevo producto", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btInsertActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código del producto", "Eliminar producto", JOptionPane.QUESTION_MESSAGE);
        if(codigo != null)
        {
            int response = LogicController.isProductoInOrden(codigo);
            
            switch(response)
            {
                case -2:
                {
                    JOptionPane.showMessageDialog(this, "Ha ocurrido un error al consultar el producto", "Eliminar producto", JOptionPane.ERROR_MESSAGE);
                    
                    break;
                }
                
                case -1:
                {
                    JOptionPane.showMessageDialog(this, "El producto que intentas eliminar no existe", "Eliminar producto", JOptionPane.WARNING_MESSAGE);
                    
                    break;
                }
                
                case 0:
                {
                    boolean executed = LogicController.deleteProducto(codigo);

                    if(executed == true)
                    {
                        JOptionPane.showMessageDialog(this, "Se ha eliminado exitosamente el producto", "Eliminar producto", JOptionPane.INFORMATION_MESSAGE);

                        loadProductos();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Ha ocurrido un error al eliminar el producto", "Eliminar producto", JOptionPane.ERROR_MESSAGE);
                    }
                
                    break;
                }
                
                case 1:
                {
                    JOptionPane.showMessageDialog(this, "No se puede eliminar el producto porque está registrado en una compra", "Eliminar producto", JOptionPane.WARNING_MESSAGE);
                    
                    break;
                }
                
                default:
                {
                    break;
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debes ingresar el código del producto a eliminar", "Eliminar producto", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btRemoveActionPerformed

